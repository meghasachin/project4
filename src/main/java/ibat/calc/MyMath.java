package ibat.calc;


public class MyMath {
	
	public double testAdd(double a, double b) {
		return a+b;
	}
	
	public double testSubtract(double a, double b) {
		return Math.abs(a-b);
	}
	
	public double testMultiply(double a, double b) {
		return a*b;
	}
	
	public double testDivide(double a, double b) {
		return a/b;
	}
}
